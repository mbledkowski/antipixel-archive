		<div class="post"><h2>About Stealing These Buttons</h2>
<p>It all began on October 22, 2002 when Jeremy Hedley posted the <a href="https://web.archive.org/web/20080107200558/http://www.antipixel.com/blog/archives/2002/10/22/steal_these_buttons.html">the original offering</a> of buttons. I, as many others, were infatuated with these for quite some time and begun collecting as many as I could. Eventually my website became the depository for all things STB.  The administration backend, which allows me to stay sane while keeping a database of 3700+ buttons, was written by <a href="https://web.archive.org/web/20080107200558/http://izzywizzy.org/">Jason Bergeman</a>. You can see the creators of all these fabulous buttons on the <a href="authors.php">Authors Page</a>.</p>
<p>For other buttons check out: <a href="https://web.archive.org/web/20080107200558/http://stealthesebuttons.terrababy.nl/">Terrababy's Animated Buttons</a>, <a href="https://web.archive.org/web/20080107200558/http://www.gwtmedia.co.uk/ip.php">GWTMedia's IP Address Buttons</a>, <a href="https://web.archive.org/web/20080107200558/http://personal.nbnet.nb.ca/bemartin/80x15/">Freestyle Buttons</a>.</p>
</div>
<div class="post">
<h2>Making Your Own</h2>
<p>There are many resources available to make your own button. Adam Kalsey has a <a href="https://web.archive.org/web/20080107200558/http://www.kalsey.com/tools/buttonmaker/">Button Maker</a> and Pierre has a more <a href="https://web.archive.org/web/20080107200558/http://ekstreme.com/buttonmaker/index.php">robust one</a> which are basically web GUIs for Bill Zeller's <a href="https://web.archive.org/web/20080107200558/http://www.minimalverbosity.com/2003/May/19/buttons.htm">script</a>. The official font of STB is 6px <a href="https://web.archive.org/web/20080107200558/http://kottke.org/plus/type/silkscreen/">Silkscreen</a> (by <a href="https://web.archive.org/web/20080107200558/http://kottke.org/">Jason Kottke</a>). It is best when aliasing is turned off in Photoshop. Once you have your button all nice 'n shiny head over to the <a href="make.php">Upload Form</a>.</p>

